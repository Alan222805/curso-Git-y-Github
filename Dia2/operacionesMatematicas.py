x = 2
y = 5

print(f"La suma de {x} y {y} es: {x+y}") # --> La suma de 2 y 5 es: 7

print(f"La resta de {x} y {y} es: {x-y}") # --> La resta de 2 y 5 es: -3

print(f"La multiplicacion de {x} y {y} es: {x*y}") # --> La multiplicacion de 2 y 5 es: 10

print(f"La division de {x} y {y} es: {x/y}") # --> La division de 2 y 5 es: 0.4

print(f"El modulo de {y} y {x} es: {y%x}") # --> El modulo de 2 y 5 es: 1

print(f"La potencia de {x} a la {y} es: {x**y}") # --> La potencia de 2 a la 5 es: 32

print(f"La division al piso de {x} y {y} es: {x//y}") # --> La division al piso de 2 y 5 es: 0

print(f"La raiz cuadrada de {x} es: {x**0.5}") # --> La raiz cuadrada de 2 es: 1.4142135623730951

